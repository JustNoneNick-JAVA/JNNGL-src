package com.jnngl.library.handlers;

public class IntegerHandler {
	
	public String intToString(int x) {
		return "" + x;
	}
	
	public double intToDuble(int x) {
		return (double) x;
	}
	
	public float intToFloat(int x) {
		return (float) x;
	}
	
	public short intToShort(int x) {
		return (short) x;
	}
	
	public long intToLong(int x) {
		return (long) x;
	}
	
	public byte intToByte(int x) {
		return (byte) x;
	}
	
}
